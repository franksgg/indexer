ALTER FUNCTION NEXTPAUSE RETURNS D_VC256
AS 
declare variable SLEN D_SMALLINT;
declare variable SDUR D_SMALLINT;
declare variable ACTPATH D_VC256;
begin
  select S.SILENCE
  from SYSDATA S
  where ID = 1
  into :SLEN;
  select coalesce(D.DURATION, 0)
  from NDELAY D
  where ID = 1
  into :SDUR;
  if (SDUR = 0) then
  begin
    update or insert into NDELAY (ID, DURATION)
    values (1, :SLEN)
    matching (ID);
    ACTPATH = NEXTTRACK();
  end
  else
  begin
    SDUR = SDUR - 1;
    ACTPATH = '/var/data/music/silence.mp3';
    update or insert into NDELAY (ID, DURATION)
    values (1, :SDUR)
    matching (ID);
  end
  return ACTPATH;
end ^

ALTER FUNCTION NEXTTRACK RETURNS D_VC256
AS 
DECLARE M D_FK;
DECLARE A D_FK;
DECLARE MW D_FK;
DECLARE WP D_FK;
DECLARE ACTPATH D_VC256;
DECLARE NTNR D_FK;
begin
  select S.PLMAX
  from SYSDATA S
  where ID = 1
  into :M;
  select max(ID)
  from WL
  into :MW;
  if (:MW is null) then --wishlist is empty
    update ACTPOS
    set WP = null;
  select first 1 WP,
                 AP
  from ACTPOS
  into :WP, :A;
  if (WP is null) then
  begin
    if (A = M) then
    begin
      A = 1;
    end
    else
      A = A + 1;
    update ACTPOS
    set AP = :A;
  end
  else
  begin
    if (WP = MW) then
    begin
      WP = null;
    end
    else
      WP = WP + 1;
    update ACTPOS
    set WP = :WP;
  end
  NTNR = TNR(1);
  select coalesce(T.PATH, '/var/data/music/various/J/Je/Jethro_Tull/A_Passion_Play/01-Side_A.mp3')
  from TRACKS T
  where T.ID = :NTNR
  into :ACTPATH;
  insert into HISTORY (TNR, TRACK)
  values (:NTNR, :ACTPATH);
  update or insert into ACT_TRACK (ID, TNR)
  values (1, :NTNR)
  matching (ID);
  return ACTPATH;
end ^

