begin
  /* Procedure Text */
  CNT = 0;
  for select CH,
             ZT,
             NM,
             TT,
             AN
      from (select first 10 '' as CH,
                            H.ZEIT as ZT,
                            A.NAME as NM,
                            T.TITLE as TT,
                            coalesce(AL.NAME, 'unknown') as AN
            from HISTORY H
            join TRACKS T on T.ID = H.TNR
            join ARTISTS A on T.ARTIST_PK = A.ID
            join ALBUMS AL on T.ALBUM_PK = AL.ID
            order by H.ID desc)
      order by ZT
      into :TAG, :ZEIT, :ARTIST, :TITLE, :ALBUM
  do
  begin
    JPC = :ARTIST||' '||:ALBUM;
    JPC = replace(:JPC, ' ', '+');
    CNT = CNT + 1;
    if (CNT = 10) then
      TAG = '>';
    suspend;
  end
  select first 1 WP,
                 AP
  from ACTPOS
  into :WP, :AP;
  if (WP is not null) then
  begin
  for select first 10 ' ',
                      null,
                      A.NAME,
                      T.TITLE,
                      coalesce(AL.NAME, 'unknown')
      from WL P
      join TRACKS T on T.ID = P.FK_TRACK
      join ARTISTS A on A.ID = T.ARTIST_PK
      join ALBUMS AL on T.ALBUM_PK = AL.ID
      where P.ID > :WP
      order by P.ID
      into :TAG, :ZEIT, :ARTIST, :TITLE, :ALBUM
  do
  begin
    JPC = :ARTIST||' '||:ALBUM;
    JPC = replace(:JPC, ' ', '+');
    suspend;
  end
  end
  for select first 10 ' ',
                      null,
                      A.NAME,
                      T.TITLE,
                      coalesce(AL.NAME, 'unknown')
      from pL P
      join TRACKS T on T.ID = P.FK_TRACK
      join ARTISTS A on A.ID = T.ARTIST_PK
      join ALBUMS AL on T.ALBUM_PK = AL.ID
      where P.ID > :AP
      order by P.ID
      into :TAG, :ZEIT, :ARTIST, :TITLE, :ALBUM
  do
  begin
    JPC = :ARTIST||' '||:ALBUM;
    JPC = replace(:JPC, ' ', '+');
    suspend;
  end

end ^

ALTER PROCEDURE SPLIT_STRING (P_STRING TYPE OF D_VCLONG,
P_SPLITTER CHAR(1))
RETURNS (PART TYPE OF D_VCLONG)
AS 
declare variable lastpos integer;
  declare variable nextpos integer;
begin
    p_string = :p_string || :p_splitter;
    lastpos = 1;
    nextpos = position(:p_splitter, :p_string, lastpos);
    if (lastpos = nextpos) then
        begin
            part = substring(:p_string from :lastpos for :nextpos - :lastpos);
            suspend;
            lastpos = :nextpos + 1;
            nextpos = position(:p_splitter, :p_string, lastpos);
        end
    while (:nextpos > 1) do
        begin
            part = substring(:p_string from :lastpos for :nextpos - :lastpos);
            lastpos = :nextpos + 1;
            nextpos = position(:p_splitter, :p_string, lastpos);
            suspend;
        end
end ^

