ALTER PROCEDURE GETMETA RETURNS (TITLE D_VC256,
ARTIST D_VC256,
ALBUM D_VC256,
LEN D_INT,
PYEAR D_VC128,
PERCENT D_INT,
ATIME D_INT,
IMGSRC D_VC256,
IMG D_IMAGE,
TEMP D_DOUBLE,
HUM D_DOUBLE,
WIND D_DOUBLE,
VERBRAUCH D_DOUBLE,
PRODUKTION D_DOUBLE,
CINFO D_VC128)
AS 
DECLARE T D_TIME;
DECLARE TID D_FK;
DECLARE RID D_FK;
DECLARE WINFO D_BOOLEAN;
begin
  select first 1
      STARTTIME
  from ACT_TRACK into :T;
  RID = 1;
  TEMP = 0;
  HUM = 0;
  WIND = 0;
  select
      TR.TNR
  from ACT_TRACK TR
  where
      TR.ID = 1 into :TID;
  --TID = TNR(:RID);
  select
      coalesce(A.NAME, 'unknown'),
      coalesce(T.TITLE, 'unknown'),
      coalesce(AL.NAME, 'unknown'),
      coalesce(T.LEN, 60) + coalesce(S.SILENCE, 0),
      coalesce(coalesce(T.PYEAR, AL.PYEAR), 'unknown'),
      case S.IMGSRC
        when 1 then coalesce(coalesce(ARI.MIMETYPE, ARA.MIMETYPE, ART.MIMETYPE), UK.MIMETYPE)
        when 2 then coalesce(coalesce(ART.MIMETYPE, ARA.MIMETYPE, ARI.MIMETYPE), UK.MIMETYPE)
        else coalesce(coalesce(ARA.MIMETYPE, ARI.MIMETYPE, ART.MIMETYPE), UK.MIMETYPE)
      end,
      case S.IMGSRC
        when 1 then coalesce(coalesce(ARI.IMAGE, ARA.IMAGE, ART.IMAGE), UK.IMAGE)
        when 2 then coalesce(coalesce(ART.IMAGE, ARA.IMAGE, ARI.IMAGE), UK.IMAGE)
        else coalesce(coalesce(ARA.IMAGE, ARI.IMAGE, ART.IMAGE), UK.IMAGE)
      end
  from TRACKS T
  join
      ARTISTS A on T.ARTIST_PK = A.ID
  left join
      ALBUMS AL on T.ALBUM_PK = AL.ID
  left join
      IMAGES ARI on A.FK_IMAGE = ARI.ID
  left join
      IMAGES ARA on AL.FK_IMAGE = ARA.ID
  left join
      IMAGES ART on T.IMAGE_PK = ART.ID
  left join
      IMAGES UK on UK.ID = -1
  left join
      SYSDATA S on S.ID = :RID
  where
      T.ID = :TID into :ARTIST, :TITLE, :ALBUM, :LEN, :PYEAR, :IMGSRC, :IMG;
  ATIME = datediff(second from :T to localtime);
  PERCENT = ATIME * 100 / LEN;
  
  SUSpEND;
end ^

